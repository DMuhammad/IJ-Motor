    private void bt_beliMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bt_beliMouseClicked
        // TODO add your handling code here:
        int id = sr.getId();
        int stok = 0;
        sr = new showroom(id, stok);
        
        sdf = new SimpleDateFormat("dd MMMM yyyy");
        String tgl = sdf.format(Calendar.getInstance().getTime());
        String username = tr.getUsername();
        String merek = jtf_merek.getText();
        String jenis = jtf_jenis.getText();
        String tipe = jtf_tipe.getText();
        int cc = Integer.parseInt(jtf_cc.getText());
        String warna = jtf_warna.getText();
        int harga = Integer.parseInt(jtf_harga.getText());
        tr = new transaksi(username, merek, tipe, cc, harga, tgl);
        
        if(merek.equals("") || jenis.equals("") ||
            tipe.equals("") || warna.equals("")){
            JOptionPane.showMessageDialog(rootPane, "TIDAK DITEMUKAN DATA MOTOR UNTUK DIBELI !!!");
        }else{
            try{
                database = new database();
                conn = Koneksi.koneksi();
                pst = conn.prepareStatement(database.insertDataTransaksi(tr));
                
                int confirm = JOptionPane.showConfirmDialog(this, "Apakah Anda Ingin Membeli Motor Ini ?");

                if(confirm == JOptionPane.YES_OPTION){
                    pst.execute();
                    conn.prepareStatement(database.updateDataMotor(sr)).execute();
                    JOptionPane.showMessageDialog(rootPane, "Pembelian Berhasil");
                    tampilDataDashboard();
                    tampilDataPembelian();
                }else if(confirm == JOptionPane.NO_OPTION){
                    
                } 
            }catch(Exception e){
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_bt_beliMouseClicked

