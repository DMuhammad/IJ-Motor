    private void bt_tambahMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bt_tambahMouseClicked
        // TODO add your handling code here:
        String merek = cb_merek.getSelectedItem().toString();
        String jenis = cb_jenis.getSelectedItem().toString();
        String tipe = jtf_tipe.getText();
        int tahun = Integer.parseInt(jtf_tahun.getText());
        int cc = Integer.parseInt(cb_cc.getSelectedItem().toString());
        String warna = jtf_warna.getText();
        String stnk = cb_stnk.getSelectedItem().toString();
        String bpkb = cb_bpkb.getSelectedItem().toString();
        String pajak = cb_pajak.getSelectedItem().toString();
        int stok = Integer.parseInt(jtf_stok.getText());
        int harga = Integer.parseInt(jtf_harga.getText());
        
        data = new showroom(merek, jenis, tipe, tahun, cc, warna, stnk, bpkb, pajak, stok, harga);
        
        if(tipe.equals("") || warna.equals("")){
            JOptionPane.showMessageDialog(rootPane, "DATA HARUS LENGKAP !!!");
        }else{
            try{
                database = new database();
                conn = Koneksi.koneksi();
                pst = conn.prepareStatement(database.insertDataMotor(data));
                
                int confirm = JOptionPane.showConfirmDialog(this, "Apakah Data Telah Sesuai ?");
                
                if(confirm == JOptionPane.YES_OPTION){
                    pst.execute();
                    jtf_tipe.setText("");
                    jtf_tahun.setText("2010");
                    jtf_warna.setText("");
                    jtf_harga.setText("5000000");
                    tampilDataDashboard();
                    JOptionPane.showMessageDialog(rootPane, "DATA BERHASIL DITAMBAHKAN");
                }else if(confirm == JOptionPane.NO_OPTION){
                    JOptionPane.showMessageDialog(rootPane, "Silahkan Lengkapi Data Anda");
                }
            }catch(Exception e){
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_bt_tambahMouseClicked

