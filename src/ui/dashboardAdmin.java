    public void tampilDataDashboard() {
        model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Merek");
        model.addColumn("Jenis");
        model.addColumn("Tipe");
        model.addColumn("Tahun");
        model.addColumn("CC");
        model.addColumn("Warna");
        model.addColumn("STNK");
        model.addColumn("BPKB");
        model.addColumn("Pajak");
        model.addColumn("Stok");
        model.addColumn("Harga");

        try {
            database = new database();
            conn = Koneksi.koneksi();
            pst = conn.prepareStatement(database.queryAllDataMotor());
            rst = pst.executeQuery();

            while (rst.next()) {
                model.addRow(new Object[]{
                    rst.getString("id"),
                    rst.getString("merek"),
                    rst.getString("jenis"),
                    rst.getString("tipe"),
                    rst.getString("tahun"),
                    rst.getString("cc"),
                    rst.getString("warna"),
                    rst.getString("stnk"),
                    rst.getString("bpkb"),
                    rst.getString("pajak"),
                    rst.getString("stok"),
                    rst.getString("harga")
                });
            }
            jTable1.setModel(model);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
